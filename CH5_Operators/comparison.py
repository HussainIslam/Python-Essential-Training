x = 42
y = 73

#Less than (<) operator
print('Less than (<) opeator: ')
print(f'Comparison is true') if x < y else print('Comparison is false')

#Greater than (>) operator
print('Greater than (>) opeator: ')
print(f'Comparison is true') if x > y else print('Comparison is false')

#Equality (==) operator
print('Equality (==) opeator: ')
print(f'Comparison is true') if x == y else print('Comparison is false')

#Not Equal (!=) operator
print('Not Equal (!=) opeator: ')
print(f'Comparison is true') if x != y else print('Comparison is false')